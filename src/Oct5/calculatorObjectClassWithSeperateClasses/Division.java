package Oct5.calculatorObjectClassWithSeperateClasses;

public class Division {
    public double division(double a,double b){
        return a / b;
    }

    public double division(double a,double b, double c){
        return a / b / c;
    }

    public double division(double a,double b, double c, double d){
        return a / b / c / d;
    }
}

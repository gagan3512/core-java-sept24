package Oct5.calculatorObjectClassWithSeperateClasses;

public class Multiplication {

    public double multiplication(double a,double b){
        return a * b;
    }

    public double multiplication(double a,double b, double c){
        return a * b * c;
    }

    public double multiplication(double a,double b, double c, double d){
        return a * b * c * d;
    }
}
